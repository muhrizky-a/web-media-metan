                <p><strong>JAKARTA, TAMBANG – </strong>Kementerian Energi dan Sumber Daya Mineral (ESDM) mengajak Kementerian dan Lembaga pemerintahan untuk mempercepat program nasional, yaitu penerapan Kendaraan Bermotor Listrik Berbasis Baterai (KBLBB) untuk transportasi jalan, khususnya kendaraan roda dua.</p>
                <p>Plt Kepala Badan Litbang ESDM, Dadan Kusdiana mengatakan hal ini guna mengakselerasi penerapan KBLBB roda dua. Menurutnya, Grand Strategi Energi Nasional (GSEN) menargetkan 13 juta unit KBLBB roda dua pada tahun 2030. Untuk merealisasikannya, Kementerian ESDM menginisiasi program konversi sepeda motor BBM menjadi sepeda motor listrik.</p>
                <p>"Pada tahun 2021 sejumlah 100 unit sepeda motor listrik dinas operasional KESDM yang nilai bukunya sudah Rp.0, dikonversi menjadi sepeda motor listrik” ujar Dadan pada Sosialisasi Program Konversi Sepeda Motor BBM menjadi Sepeda Motor Listrik di lingkungan Kementerian dan Lembaga, di Bogor, dikutip Selasa (25/1).</p>
                <p>Menurut Dadan, Konversi dilakukan di Workshop Ketenagalistrikan Pusat Penelitian dan Pengembangan Teknologi Ketenagalistrikan, Energi Baru, Terbarukan dan Konservasi Energi (P3TKEBTKE). Workshop ini sudah disertifikasi sebagai Bengkel Pemasangan, Perawatan, dan Pemeriksaan Peralatan Instalasi Sistem Penggerak Motor Listrik pada Kendaraan Bermotor oleh Kementerian Perhubungan.</p>
                <p>P3TEKEBTKE, kata dia telah menyelesaikan uji jalan 10.000 km untuk ketahanan (endurance test) dan pengujian ke Balai Pengujian Laik Jalan dan Sertifikasi Kendaraan Bermotor. Surat BPKB/STNK sepeda motor juga telah disesuaikan di Korps Lalu Lintas (Korlantas) POLRI.</p>
                <p>Dadan menguraikan pengguna sepeda motor konversi menghemat pengeluaran biaya BBM Rp. 2,78 juta/ tahun, belum termasuk biaya ganti oli. Jika saat ini populasi kendaraan roda dua di Indonesia sebanyak 115 juta (data BPS 2020), maka program ini berpotensi menghasilkan efisiensi penggunaan BBM Rp. 319 triliun rupiah/tahun serta menurunkan emisi gas rumah kaca sebesar 65 juta ton CO2.</p>
                <p>"Untuk memenuhi target GSEN tahun 2030 sejumlah 13 juta unit kendaraan roda 2, maka potensi penghematan BBM sekitar Rp.36 triliun per tahun dan menurunkan emisi GRK 7,5 juta ton CO2e per tahun", ungkapnya.</p>
                <p>Kata Dadan, Kementerian ESDM akan memposisikan diri sebagai katalisator program konversi sepeda motor dan siap bekerja sama dengan kementerian/lembaga. Kesuksesan program ini, akan membuka pasar dan mempercepat laju penerapan pada masyarakat, sehingga akan turut menggiatkan perekonomian nasional dengan melibatkan sektor UMKM dan industri lokal.</p>
                <p>Pada kesempatan yang sama, Tenaga Ahli Menteri ESDM Bidang Ketenagalistrikan, Sripeni Inten Cahyani menjelaskan, pemerintah menargetkan enam juta kendaraan roda dua dapat dikonversi hingga lima tahun mendatang.</p>
                <p>"Untuk mencapainya, Kementerian ESDM akan mengusulkan program konversi menjadi mandatori bagi kendaraan operasional kementerian/lembaga dan pemerintah daerah, yang dianggarkan dalam APBN,” paparnya.</p>
                <p>Inten menambahkan, berdasarkan data awal, sepeda motor kendaraan operasional di kementerian dan lembaga, sebagian besar diperoleh dari periode 2000-2010 dan 2010-2019. Kendaraan yang dibidik untuk dikonversi adalah sepeda motor BBM kendaraan operasional layak jalan dengan nilai buku Rp 0.</p>
                <p>Untuk mencapai target prototipe 1.000 unit sampai enam juta kendaraan, Inten berharap kementerian/lembaga dapat mempelopori program ini dengan anggaran antara Rp.9-10 juta/unit.</p>
